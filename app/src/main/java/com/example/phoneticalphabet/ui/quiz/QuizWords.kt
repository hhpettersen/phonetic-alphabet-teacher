        /**
         * Can be used to validate how many words each letter has in the word-list
         *
         * @param words
         * @return
         */
        fun grouped(words: List<String>): MutableList<Pair<Char, Int>> {
            var currentLetter: Char = 'a'
            var currentCount: Int = 0
            var groups: MutableList<Pair<Char, Int>> = mutableListOf()

            words.forEachIndexed { index, it ->
                if (index == words.size - 1) {
                    groups.add(Pair(currentLetter, currentCount))
                }
                if (it.first() == currentLetter) {
                    currentCount++
                } else {
                    groups.add(Pair(currentLetter, currentCount))
                    currentLetter = it.first()
                    currentCount = 0
                }
            }
            return groups
        }
